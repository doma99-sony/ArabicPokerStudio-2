/**
 * الحصول على عدد محدد من خطوط الدفع النشطة
 * @param count عدد خطوط الدفع المراد تفعيلها
 * @returns مصفوفة من خطوط الدفع المفعلة
 */
export function getActivePaylines(count: number): Payline[] {
  // التأكد من أن العدد ضمن النطاق المسموح (10-20)
  count = Math.max(10, Math.min(20, count));
  
  // ترتيب خطوط الدفع حسب الأولوية (الخطوط الرئيسية أولاً)
  return paylines.slice(0, count);
}